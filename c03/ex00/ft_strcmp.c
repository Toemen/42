#include <unistd.h>
#include <stdio.h>
#include <string.h>

int		ft_strcmp(char *s1, char *s2)
{
	int c;

	c = 0;
	while (s1[c] == s2[c])
	{
		if (s1[c] == '\0')
			return (0);
		c++;
	}
	return (s1[c] - s2[c]);
}
