#include <unistd.h>
#include <stdio.h>

int			ft_str_is_alpha(char *str)
{
	while (str[0] != '\0')
	{
		if (!(str[0] >= 65 && str[0] <= 90) && !(str[0] >= 97 && str[0] <= 122))
			return (0);
		str++;
	}
	return (1);
}
